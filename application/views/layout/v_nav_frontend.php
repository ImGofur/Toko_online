						<script>
						const slider = document.querySelector('.slider');
						const prevBtn = document.getElementById('prevBtn');
						const nextBtn = document.getElementById('nextBtn');
						let currentIndex = 0;
						let autoSlideInterval; // Variabel untuk menyimpan interval autoplay

						// Fungsi untuk menggeser slide ke kanan
						function nextSlide() {
							if (currentIndex < 2) {
								currentIndex++;
							} else {
								currentIndex = 0;
							}
							slide();
						}

						// Fungsi untuk menggeser slide ke kiri
						function prevSlide() {
							if (currentIndex > 0) {
								currentIndex--;
							} else {
								currentIndex = 2;
							}
							slide();
						}

						// Fungsi untuk menggeser slide
						function slide() {
							slider.style.transform = `translateX(-${currentIndex * 100}%)`;
						}

						// Tambahkan autoplay dengan interval 3 detik (3000 ms)
						function startAutoSlide() {
							autoSlideInterval = setInterval(nextSlide, 3000); // Ganti slide setiap 3 detik
						}

						// Hentikan autoplay
						function stopAutoSlide() {
							clearInterval(autoSlideInterval);
						}

						// Event listener untuk tombol next dan prev
						nextBtn.addEventListener('click', () => {
							nextSlide();
							stopAutoSlide(); // Hentikan autoplay saat pengguna mengklik tombol
						});

						prevBtn.addEventListener('click', () => {
							prevSlide();
							stopAutoSlide(); // Hentikan autoplay saat pengguna mengklik tombol
						});

						// Mulai autoplay saat halaman dimuat
						startAutoSlide();



